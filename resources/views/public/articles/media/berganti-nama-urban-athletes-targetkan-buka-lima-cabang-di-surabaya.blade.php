            SURABAYA (suarakawan.com) – Kesadaran masyarakat khususnya di kota-kota besar untuk hidup sehat terus meningkat. Ini dapat dilihat dengan kian maraknya kehadiran pusat kebugaran tubuh, tak terkecuali di Surabaya. Tak sedikit warga Surabaya yang memilih mengunjungi pusat kebugaran tubuh sebagai upaya menciptakan gaya hidup sehat. Fenomena ini pun juga kian memotivasi pusat-pusat kebugaran sebagai upaya untuk terus meningkatkan pelayanannya. Salah satunya adalah Urban Fitness yang kini memilih berganti nama menjadi Urban Athletes.<br><br>

            “Setelah satu tahun sejak pertama kali didirikan Oktober 2016 lalu, Urban Fitness telah menjadi salah satu pilihan utama masyarakat Surabaya dalam memilih tempat latihan kebugaran atau fitness center. Di usianya yang menginjak satu tahun ini, Urban Fitness hadir dengan semangat baru melalui logo dan nama barunya, yakni Urban Athletes,” terang Verawati Budianto, owner Urban Athletes, Minggu (22/10).<br><br>

            Lebih lanjut Vera mengungkapkan, nama Urban Athletes dipilih karena Urban Athletes percaya setiap orang adalah atlet dalam kesehariannya berolahraga. Juga diharapkan nama atau sebutan ‘athletes’ tersebut dapat lebih memotivasi para anggota untuk lebih giat berolahraga, tak hanya untuk memenuhi tren gaya hidup tetapi juga untuk menjaga kesehatan.<br><br>

            “Urban Athletes percaya bahwa kesehatan yang terlihat luar berasal dari kesehatan yang telah diolah dari dalam. Seiring berkembangnya zaman dan kesibukan yang terus meningkat,” imbuhnya.<br><br>

            Urban Athletes, lanjutnya, melihat semakin banyak orang yang tidak peduli dengan kesehatan. Hal ini menjadikan motivasi bagi Urban Athletes untuk memberikan tempat khusus bagi mereka yang ingin menjadikan hidup sehat sebagai gaya hidup dengan metode yang tepat dan fun.<br><br>

            Ada berbagai layanan yang ditawarkan di Urban Athletes. Diantaranya adalah personal Trainer dengan sertifikasi Lessmills. Trainer profesional Urban Athletes akan membantu member secara tepat dan cepat dengan standarisasi yang telah ditentukan. Konsultasi fitness dan kesehatan juga dapat dilakukan dengan para trainer.<br><br>

            “Para member juga mendapatkan akses ke lebih dari 152 kelas. Kelas-kelas ini dapat diikuti oleh para member Urban Athletes, dan dipimpin oleh pelatih-pelatih berpengalaman di bidangnya,” tukasnya.<br><br>

            Dalam satu tahun kiprahnya, Urban Athletes telah dibuka di dua lokasi, yaitu di Apartemen Gunawangsa MERR, serta di Marvell City Surabaya, dengan jumlah member aktif sebanyak 1.400 orang. Hingga tahun depan, Urban Athletes menargetkan untuk memiliki lima cabang di Surabaya.<br><br>

            “Semakin banyak cabang kami akan kian memudahkan member untuk berolahraga sesuai dengan waktu dan kebutuhan mereka,” simpul Vera. (ris/rur)<br><br>
