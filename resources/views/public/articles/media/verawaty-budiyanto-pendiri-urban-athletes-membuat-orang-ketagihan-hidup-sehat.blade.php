            <p><strong>Meski baru didirikan September 2016, Urban Athletes telah menjadi salah satu tempat latihan kebugaran atau fitness center pilihan utama masyarakat Surabaya. Ribuan member telah terdatar di Urban Athletes yang memiliki dua tempat kebugaran, yakni di Apartemen Gunawangsa MERR dan Marvell City Surabaya.</strong><br><br>

            Pemilihan nama ‘Athletes’ dilandasi filosofi bahwa setiap orang adalah atlet dalam keseharian berolahraga. Juga agar lebih memotivasi para anggota lebih giat berolahraga. Tak hanya sekadar tren gaya hidup, tetapi juga menjaga kesehatan.<br><br>

            Perlahan namun pasti, Verawaty Budiyanto mulai merealisasi impiannya untuk memiliki tempat latihan kebugaran atau fitness center yang menjadikan para membernya makin peduli terhadap kesehatan. Urban Athletes dipatoknya menjadi ‘gym yang peduli dengan hasil dari  membernya’.<br><br>

            <strong>Vera bersama kedua anaknya dan staf pada pembukaan Urban Athletes Marvel City Surabaya.</strong><br>
            Pada Sabtu (3/3), Urban Athletes memperkenalkan The Valor yang merupakan kelas terbaru.  The Valor disiapkan agar para Athletes –sebutan bagi para member—memiliki motivasitinggi untuk mencapai apa yang diinginkan ketika pertama kali memutuskanmenjadi member.<br><br>

            “Pada gym kedua ini saya bikin konsep baru. Saya banyak menemukan member yang duduk di alat sambil mainan hape. Ada juga yang dating hanya sekadar rutinitas saja, sehingga timbul rasa bosan. Jadi banyak yang dating tapi apa yang diinginkan saat memutuskan masuk gym tak tercapai,” kata Vera.<br><br>

            <strong>Vera (kanan) latihan kickboxing bersama Personal Trainer.</strong>
            Strategi agar para Athletes terjaga motivasinya adalah membuat kelas-kelas yang didampingi instruktur khusus. Para instruktur ini berkewajiban menjaga hubungan baik dan memahami apa yang diinginkan seorang Athletes. Termasuk membangun komunikasi di dalam sebuah kelas, sehingga setiap Athletes merasa nyaman dan termotivasi mencapai target personalnya, baik untuk perbaikan, pengencangan, atau pelangsingan tubuh.<br><br>

            “Nanti orang datang ikut kelas. Bakal ada komunikasi yang intens dengan staf kita, sehingga motivasi selalu terjaga. Kalau mereka dating gak ketemu siapa-siapa ya malas. Sementara para instruktur harus memahami setiap anggota kelas sehingga hubungan lebih dekat dan memaksa orang datang,” papar Vera.<br><br>

            Urban Athletes telah menyiapkan 30 instruktur profesional yang tak sekadar memahami olah tubuh, tapi juga memiliki komunikasi yang baik dengans emua orang.<br><br>


            <strong>Suasana kelas sepeda statis RPM (Row Power of Motion).</strong>
            “The Valor dilakukan dalam intensitas rutin yang nantinya akan merangsang para Athletes untuk terus penasaran danbisa naik level,” tutur Verawaty.<br><br>

            Setelah di Marvel City dan Apartemen Gunawangsa MERR, Vera sedang mempersiapkan cabang baru di Gunawangsa Tidar, tahun ini. “Impiansaya selanjutnya membuka di Surabaya barat,” kata Vera.<br><br>

            Lalu apa yang memotivasi Vera untuk terus mengembangkan Urban Athletes?<br><br>

            Veratelah lama memiliki impian membuat gym dengan aturan yang dia buat sendiri. Gym yang penuh kehangatan bagi setiap membernya. Sebab setiap orang memiliki karakterter sendiri yang unik dan spesial.<br><br>

            “Target saya, setiap orang bisamencapai fitness goal-nya dan menjadi manusia yang lebih baik dengan cara yang sangat menyenangkan dan tidak membosankan. Sebab gym bisa membuat orang memiliki kualitas hidup lebih baik secara fisik maupun emosional,” katanya<br><br>

            Vera pun menceritakan awal ia berkecimpung di bisnis gym, saat menjadi sales consultant, pada Oktober 13 tahun lalu. “Istilah bekennya SPG. Saya masih ingat nomor   547, artinya saya karyawan ke-547 dari Celebrity Fitness. Saya langsung jatuh cinta pada industry kebugaran karena sifatnya yang dinamis,” katanya.<br><br>

            Selama berkecimpung, Vera mengaku menemukan banyak orang yang menemukan kembali hidupnya hanya karena gym dan setelah membuat satu keputusan penting dalamhidupnya. Yakni memutuskan rutin berolahraga dan memulai pola hidup sehat.<br><br>

            “Ada seorang yang mengidap fatty liver (pembengkakan hati akibat penimbunan lemak)  sembuh total karena rutin RPM (Rapid Power Motion) atau membakar lemak dan kalori dengan cara mengayuh sepeda statis. Ada orang beratnya 129 kg menjadi 78 kg dalam waktu kurang dari 5 bulan,” paparnya.<br><br>

            Ada juga, lanjut Vera, yang tidak pernah bisa jalan lama karena lower back, kemudian bisa ikut lari  maratonkarena rajin yoga. Ada yang sakit diabetes dan rutin pakai insulin, lalu bisa lepas dari insulin hanya karenarajin latihan beban.<br><br>

            “Saya sendiri penderita Hernia Nucleus Pulposus (HNP) atau syaraf kejepit. Saya tidak pernah bisa duduk atau berdiri lama tanpa harus sakit pinggang. Sekarang sudah sembuh karena rajin weight training. Masih banyak lagi testimoni yang membuat saya semakin yakin berada di industri yang tepat,” pungkasnya. <br><br>
