            <p><strong>Surabaya, Bhirawa</strong><br>
            Kesibukan masyarakat Surabaya yang kian padat, kerap menjadi alasan mereka untuk tidak bisa menjaga kebugaran sehari-hari. Kini kendala itu coba diatasi oleh Urban Athletes dengan mengenalkan virtual fitness studio pertama di Jatim, yakni kelas kebugaran tanpa instruktur langsung di lokasi.<br><br>

            “Virtual fitness studio ini makin populer di negara lain, tapi di Jatim baru pertama ini kami kenalkan. Model ini cocok bagi warga yang memiliki kesibukan tinggi, tak bisa mengikuti jadwal kelas dengan instruktur yang sudah disediakan pusat kebugaran,” kata CEO Urban Athletes Verawati Budianto di sela trial pembukaan Urban Athletes di Marvell City Surabaya, Sabtu (25/11).<br><br>

            Dijelaskan Vera, dengan layar lebar member bisa mengikuti gerakan-gerakan fitness yang ditampilkan pada layar dengan teknologi visual. Untuk model fitness ini, Urban Athletes bekerjasama dengan LesMills, sebuah grup exercise provider dari Selandia Baru yang menjadwalkan kelas kebugaran update terbaru dengan pelatih tersertifikasi. “Karena virtual, maka kelas fitness ini bisa live dan tayang di beberapa negara, tak hanya di Surabaya. Jadwal kelas menyesuaikan dengan jadwal kelas yang ada di LesMills. Jadi ini fitness live juga, hanya instrukturnya tidak di Surabaya,” ujarnya.<br><br>

            Kelas fitness yang ada di virtual studio ada beragam jenis, mulai BodyPump, BodyCombat, BodyBalance, SH’BAM, RPM dan Sprint. Kelas ini dihadirkan untuk menyesuaikan dengan keinginan member, dan tubuh bagian mana yang mau dibentuk.<br><br>

            Kemarin model kebugaran baru ala virtual fitness studio itu dikenalkan secara langsung. Menampilkan layar besar, satu instruktur Urban Athletes terlihat serius mengikuti gerakan yang ditampilkan instruktur asing dalam layar film.<br><br>

            Hadirnya virtual studio ini diapresiasi positif sejumlah warga yang hadir. Seperti diakui Retno, warga Surabaya. Dia menilai virtual studio lebih menguntungkan bagi mereka yang sibuk daripada fitness konvensional. “Kalau yang konvensional, member menyesuaikan dengan jadwal yang telah disusun pusat kebugaran. Dengan virtual studio, tak ada batasan jadwal dan waktu lagi. Setiap saat kita mau fitness bisa dengan instruktur asing,” katanya.&nbsp;<strong>[tis]</strong></p>

