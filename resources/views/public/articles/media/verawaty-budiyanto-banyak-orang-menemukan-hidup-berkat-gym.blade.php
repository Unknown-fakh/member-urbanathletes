                Surabaya, penamerahputih.com – Urban Fitness merayakan setahun usianya dengan nama dan logo baru Urban Athletes. Meski belia, Urban Athletes telah menjadi salah satu tempat latihan kebugaran atau fitness center pilihan utama masyarakat Surabaya.<br><br>

                Ratusan member telah terdatar di Urban Athletes yang memiliki dua tempat kebugaran, yakni di Apartemen Gunawangsa MERR dan Marvell City Surabaya.<br><br>

                Pemilihan nama ‘Athletes’ dilandasi filosofi bahwa setiap orang adalah atlet dalam keseharian berolahraga. Juga agar lebih memotivasi para anggota lebih giat berolahraga. Tak hanya sekedar tren gaya hidup, tetapi juga menjaga kesehatan.<br><br>

                “Urban Athletes melihat semakin banyak orang yang tidak peduli dengan kesehatan. Hal ini menjadi motivasi kami untuk memberikan tempat khusus bagi mereka yang ingin menjadikan hidup sehat sebagai gaya hidup dengan metode yang tepat dan fun,” kata Verawaty Budiyanto, pendiri dan pemilik Urban Athletes saat peresmian nama dan logo baru, di Apartemen Gunawangsa Merr, Minggu (22/10/2017).<br><br>

                Berikut wawancara dengan Verawaty Budiyanto:<br><br>

                <strong>Apa kesan Anda pada setahun usia Urban Fitness yang kini menjadi Urban Athletes?</strong><br><br>

                Urban Athletes is a dream comes true.<br><br>

                <strong>Kapan Anda masuk bisnis fitness center?</strong><br><br>

                Pada bulan Oktober 13 tahun lalu, saya masuk ke industri ini sebagai seorang sales consultant, istilah bekennya SPG. Saya masih ingat nomor karyawan saya 547, artinya saya karyawan ke-547 dari Celebrity Fitness. Saya langsung jatuh cinta pada industri kebugaran karena sifatnya yang dinamis.<br><br>

                <strong>Maksud Anda?</strong><br><br>

                Selama berkecimpung, saya menemukan banyak orang yang menemukan kembali hidupnya hanya karena gym dan setelah membuat satu keputusan penting dalam hidupnya, Misalnya memutuskan rutin berolahraga dan memulai pola hidup sehat.<br><br>

                Ada seorang yang mengidap fatty liver (pembengkakan hati akibat penimbunan lemak)  sembuh total karena rutin RPM (Rapid Power Motion) atau membakar lemak dan kalori dengan cara mengayuh sepeda statis. Ada orang beratnya 129 kg menjadi 78 kg dalam waktu kurang dari 5 bulan.<br><br>

                Ada juga yang tidak pernah bisa jalan lama karena lower back, kemudian bisa ikut lari maraton karena rajin Yoga. Ada yang sakit diabetes dan rutin pakai insulin, lalu bisa lepas dari insulin hanya karena rajin latihan beban.<br><br>

                <strong>Ada pengalaman pribadi?</strong><br><br>

                Saya sendiri penderita Hernia Nucleus Pulposus (HNP) atau syaraf kejepit. Saya tidak pernah bisa duduk atau berdiri lama tanpa harus sakit pinggang. Sekarang sudah sembuh karena rajin weight training. Masih banyak lagi testimoni yang membuat saya semakin yakin berada di industri yg tepat.<br><br>

                <strong>Bisa diceritakan soal Urban Athletes?</strong><br><br>

                Selama 13 tahun saya memimpikan mempunyai gym dengan aturan yang saya buat sendiri. Gym yang penuh kehangatan, di mana setiap orang adalah unik dan special. Setiap orang bisa mencapai fitness goal-nya dan menjadi manusia yang lebih baik dengan cara yang sangat menyenangkan dan tidak membosankan. Sebab gym bisa membuat orang memiliki kualitas hidup lebih baik secara fisik maupun emosional.<br><br>

                <strong>Apa kendala membesarkan Urban Athletes?</strong><br><br>

                Apabila Anda mencintai pekerjaan, maka Anda tidak perlu merasa bekerja seharipun. Dan itulah perasaan saya setiap hari bekerja di fitness center. Setahun lalu di saat yang paling tidak ideal, saya justru memulai Urban Fitness.<br><br>

                <strong>Apa kiat membangun Urban Athletes?</strong><br><br>

                Saya pegang prinsip Jack Ma,“To be successful, you need the right people with you, not the best people.” Dan hari ini saya percaya bahwa saya telah memiliki orang-orang yang tepat di sekeliling saya.<br><br>

                <strong>Apa target Anda ke depan?</strong><br><br>

                Kami berjanji untuk terus memberikan layanan yang lebih baik, terus berbenah diri untuk menjadikan Urban Athletes sebagai tempat gym yang paling dicintai oleh masyarakat Indonesia.(hag)<br><br>
